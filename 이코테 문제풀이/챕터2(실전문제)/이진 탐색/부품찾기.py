# 내가 작성한 코드

def i_zin(array, target, start, end):
    while start <= end:
        mid = (start + end) // 2
        if array[mid] == target:
            return mid
        elif array[mid] < target:
            start = mid + 1
        else:
            end = mid - 1 
    return None

n = int(input())
a = list(map(int, input().split()))
a.sort()
m = int(input())
b = list(map(int, input().split()))

for i in b:
    if i_zin(a, i, 0, n - 1) != None:
        print("Yes", end = ' ')
    else:
        print("No", end = ' ')
        
 

